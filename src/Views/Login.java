    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
        try {
            var label = new ArrayList<JLabel>();
            label.add(lblUsuario);
            label.add(lblPassword);

            var textfield = new ArrayList<JTextField>();
            textfield.add(txtUsuario);
            textfield.add(txtPassword);

            login = new LoginVM(label, textfield);
            Object[] objects = login.login();
            
            var listUsuario = (List<TUsuarios>) objects[0];
            if (!listUsuario.isEmpty()) {
                Sistema sistema = new Sistema(listUsuario.get(0));
                sistema.setVisible(true);
                sistema.setExtendedState(MAXIMIZED_BOTH);
                this.setVisible(false);
            }
        } catch (SQLException ex) {

        }
    }//GEN-LAST:event_btnIniciarSesionActionPerformed

    private void txtUsuarioKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsuarioKeyReleased
        if (txtUsuario.getText().isEmpty()) {
            lblUsuario.setForeground(new Color(70, 106, 124));
        } else {
            lblUsuario.setText("Usuario");
            lblUsuario.setForeground(new Color(0, 153, 51));
        }
    }//GEN-LAST:event_txtUsuarioKeyReleased

    private void txtPasswordKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPasswordKeyReleased
        String pass = new String(txtPassword.getPassword());
        if (pass.isEmpty()) {
            lblPassword.setForeground(new Color(70, 106, 124));
        } else {
            lblPassword.setText("Contrase침a");
            lblPassword.setForeground(new Color(0, 153, 51));
        }
    }//GEN-LAST:event_txtPasswordKeyReleased

