    private void btnAgregarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarClienteActionPerformed
        switch (TabbedPaneCliente1.getSelectedIndex()) {
            case 0:
                cliente.registrarCliente();
                break;
            case 1:
                try {
                cliente.ejecutarPago();
            } catch (SQLException ex) {
            }
            break;
            case 2:
                if (_dataUsuario.getRole().equals("Admin")) {
                    cliente.extenderDias();
                } else {
                    JOptionPane.showMessageDialog(null, "No cuenta con el permiso "
                            + "requerido para ejecutar esta acción", "Error",
                            JOptionPane.ERROR_MESSAGE);
                }
                break;
        }
    }//GEN-LAST:event_btnAgregarClienteActionPerformed

